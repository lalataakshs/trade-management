package com.example.TradeManagement.Model;

public class StockData {
    private double c;
    private double h;
    private double l;
    private int n;
    private double o;
    private long t;
    private long v;
    private double vw;

    public StockData() {
    }

    public double getC() {
        return c;
    }

    public void setC(double c) {
        this.c = c;
    }

    public double getH() {
        return h;
    }

    public void setH(double h) {
        this.h = h;
    }

    public double getL() {
        return l;
    }

    public void setL(double l) {
        this.l = l;
    }

    public int getN() {
        return n;
    }

    public void setN(int n) {
        this.n = n;
    }

    public double getO() {
        return o;
    }

    public void setO(double o) {
        this.o = o;
    }

    public long getT() {
        return t;
    }

    public void setT(long t) {
        this.t = t;
    }

    public long getV() {
        return v;
    }

    public void setV(long v) {
        this.v = v;
    }

    public double getVw() {
        return vw;
    }

    public void setVw(double vw) {
        this.vw = vw;
    }
}